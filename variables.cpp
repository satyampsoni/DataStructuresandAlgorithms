#include <iostream>
using namespace std;

int main(){
    //integer
    int x = 5;
    cout<<"Integer is "<<x<<endl;

    // float or double
    float y = 5.5;
    cout<<"float is "<<y<<endl;

    //character
    char ch = 'a';
    cout<<"character is "<<ch<<endl;

    //boolean
    bool b = true;
    cout<<b<<endl;        
}
